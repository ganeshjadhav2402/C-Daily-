#include<iostream>
using namespace std;

int main()
{
    // Switch Statement
    int i;
    cout<<"Enter the number: ";
    cin>>i;

    switch(i)
    {
        case 1:
        cout<<"Ganesh";
        break;

        case 2:
        cout<<"Tushar";
        break;

        case 3:
        cout<<"Krish";
        break;

        default:
        cout<<"Vishal";
    }
    
} 